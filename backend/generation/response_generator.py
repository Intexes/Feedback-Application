    def generate_batch(self, reviews: list) -> list:
        """Генерирует ответы для batch отзывов"""
        print(f"Генерация ответов для {len(reviews)} отзывов...")
        
        for i, review in enumerate(reviews):
            if i % 100 == 0 and i > 0:
                print(f"Сгенерировано {i}/{len(reviews)} ответов")
            
            response = self.generate_response(review)
            review["generated_response"] = response
            review["response_generated_at"] = torch.__version__  # Placeholder for timestamp
        
        print("Генерация завершена!")
        return reviews

