"""
Генератор синтетических отзывов для дообучения модели.
Создает разнообразные отзывы с разными тональностями и темами.
"""
import random
from datetime import datetime, timedelta
from typing import List, Dict, Any

class SyntheticReviewGenerator:
    """Генератор синтетических отзывов"""
    
    def __init__(self):
        self.topics = {
            "номер": ["комфорт", "чистота", "размер", "вид из окна", "кондиционер", "мебель"],
            "завтрак": ["разнообразие", "качество еды", "время работы", "очереди", "цена"],
            "персонал": ["вежливость", "скорость реакции", "профессионализм", "языки"],
            "локация": ["транспорт", "центр города", "тишина", "инфраструктура"],
            "чистота": ["ванная", "постельное белье", "общие зоны", "уборка"],
            "цена": ["соответствие качеству", "скрытые платежи", "скидки"],
            "wi-fi": ["скорость", "стабильность", "покрытие"]
        }
        
        self.templates_positive = [
            "Прекрасный {topic}! Особенно порадовал {detail}. Обязательно вернусь.",
            "Очень доволен {topic}. {detail} на высшем уровне. Рекомендую всем!",
            "{topic} превзошел ожидания. {detail} - просто отлично.",
            "Замечательный опыт благодаря {topic}. {detail} заслуживает похвалы."
        ]
        
        self.templates_negative = [
            "Ужасный {topic}. {detail} полностью разочаровал. Больше ни ногой.",
            "Не рекомендую из-за {topic}. {detail} оставляет желать лучшего.",
            "Разочарован {topic}. {detail} - это неприемлемо для такого уровня.",
            "Катастрофа с {topic}. {detail} испортил всё впечатление."
        ]
        
        self.templates_neutral = [
            "{topic} средний. {detail} ничего особенного, но и не плохо.",
            "Обычный {topic}. {detail} как везде, без сюрпризов.",
            "{topic} на троечку. {detail} могло быть лучше, но сойдет."
        ]
        
        self.details_positive = {
            "номер": ["уютная атмосфера", "новая мебель", "панорамный вид", "идеальная чистота"],
            "завтрак": ["огромный выбор блюд", "свежие продукты", "быстрое обслуживание", "вкусная выпечка"],
            "персонал": ["дружелюбные сотрудники", "помощь в любых вопросах", "знание языков", "быстрая реакция"],
            "локация": ["удобная транспортная развязка", "тихий район", "все рядом", "красивые виды"],
            "чистота": ["идеальный порядок", "ежедневная уборка", "новые полотенца", "дезинфекция"],
            "цена": ["доступная стоимость", "отличное соотношение", "приятные скидки", "прозрачные условия"],
            "wi-fi": ["высокая скорость", "стабильное соединение", "работает везде"]
        }
        
        self.details_negative = {
            "номер": ["старая мебель", "грязь в углах", "шумные соседи", "сломанный кондиционер"],
            "завтрак": ["однообразная еда", "просроченные продукты", "долгое ожидание", "грязная посуда"],
            "персонал": ["хамство сотрудников", "игнорирование просьб", "некомпетентность", "грубость"],
            "локация": ["шумная улица", "далеко от центра", "нет транспорта", "опасный район"],
            "чистота": ["грязная ванная", "пятна на белье", "пыль везде", "редкая уборка"],
            "цена": ["завышенная цена", "скрытые доплаты", "нет соответствия качеству", "грабеж"],
            "wi-fi": ["постоянно обрывается", "очень медленно", "только в лобби"]
        }
        
        self.details_neutral = {
            "номер": ["стандартная обстановка", "обычная чистота", "средний размер"],
            "завтрак": ["стандартный набор", "обычное качество", "среднее время ожидания"],
            "персонал": ["обычная вежливость", "стандартное обслуживание"],
            "локация": ["обычный район", "средняя доступность"],
            "чистота": ["приемлемая чистота", "стандартная уборка"],
            "цена": ["средняя цена", "обычные условия"],
            "wi-fi": ["работающий интернет", "средняя скорость"]
        }

    def generate(self, count: int = 3000) -> List[Dict[str, Any]]:
        """Генерирует указанное количество синтетических отзывов"""
        reviews = []
        sources = ["booking", "ostrovok", "manul", "google", "yandex"]
        
        for i in range(count):
            topic = random.choice(list(self.topics.keys()))
            detail_topic = random.choice(self.topics[topic])
            
            # Распределение: 40% позитивные, 30% негативные, 30% нейтральные
            rand_val = random.random()
            if rand_val < 0.4:
                sentiment = "positive"
                template = random.choice(self.templates_positive)
                detail = random.choice(self.details_positive.get(topic, ["хорошо"]))
                rating = random.randint(4, 5)
            elif rand_val < 0.7:
                sentiment = "negative"
                template = random.choice(self.templates_negative)
                detail = random.choice(self.details_negative.get(topic, ["плохо"]))
                rating = random.randint(1, 2)
            else:
                sentiment = "neutral"
                template = random.choice(self.templates_neutral)
                detail = random.choice(self.details_neutral.get(topic, ["нормально"]))
                rating = 3
            
            text = template.format(topic=topic, detail=detail)
            source = random.choice(sources)
            
            reviews.append({
                "id": f"synthetic_{i}",
                "source": source,
                "author": f"SyntheticUser_{i}",
                "text": text,
                "rating": rating,
                "date": (datetime.now() - timedelta(days=random.randint(0, 60))).isoformat(),
                "is_synthetic": True,
                "raw_data": {}
            })
        
        return reviews

